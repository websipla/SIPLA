BEGIN;

DROP TABLE IF EXISTS permintaan_reset_password CASCADE;
DROP TABLE IF EXISTS tanggapan CASCADE;
DROP TABLE IF EXISTS permohonan CASCADE;
DROP TABLE IF EXISTS pengaduan CASCADE;
DROP TABLE IF EXISTS kelurahan_info CASCADE;
DROP TABLE IF EXISTS jenis_layanan CASCADE;
DROP TABLE IF EXISTS petugas CASCADE;
DROP TABLE IF EXISTS masyarakat CASCADE;
DROP TABLE IF EXISTS villages CASCADE;
DROP TABLE IF EXISTS districts CASCADE;
DROP TABLE IF EXISTS regencies CASCADE;
DROP TABLE IF EXISTS provinces CASCADE;

CREATE TABLE provinces (
    id char(2) PRIMARY KEY,
    name varchar(255) NOT NULL
);

CREATE TABLE regencies (
    id char(4) PRIMARY KEY,
    province_id char(2) NOT NULL REFERENCES provinces(id) ON UPDATE CASCADE,
    name varchar(255) NOT NULL
);

CREATE TABLE districts (
    id char(7) PRIMARY KEY,
    regency_id char(4) NOT NULL REFERENCES regencies(id) ON UPDATE CASCADE,
    name varchar(255) NOT NULL
);

CREATE TABLE villages (
    id char(10) PRIMARY KEY,
    district_id char(7) NOT NULL REFERENCES districts(id) ON UPDATE CASCADE,
    name varchar(255) NOT NULL
);

CREATE TABLE masyarakat (
    nik char(16) PRIMARY KEY,
    name varchar(100) NOT NULL,
    email varchar(255) NOT NULL UNIQUE,
    username varchar(50) NOT NULL UNIQUE,
    jenis_kelamin varchar(20) NOT NULL CHECK (jenis_kelamin IN ('Laki-laki', 'Perempuan')),
    password varchar(255) NOT NULL,
    telp varchar(20) NOT NULL,
    alamat text NOT NULL,
    rt varchar(10) NOT NULL,
    rw varchar(10) NOT NULL,
    kode_pos varchar(10) NOT NULL,
    province_id char(2) NOT NULL REFERENCES provinces(id),
    regency_id char(4) NOT NULL REFERENCES regencies(id),
    district_id char(7) NOT NULL REFERENCES districts(id),
    village_id char(10) NOT NULL REFERENCES villages(id),
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE petugas (
    id_petugas bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_petugas varchar(100) NOT NULL,
    username varchar(50) NOT NULL UNIQUE,
    password varchar(255) NOT NULL,
    telp varchar(20),
    roles varchar(20) NOT NULL CHECK (roles IN ('admin', 'petugas')),
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE jenis_layanan (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama_layanan varchar(255) NOT NULL UNIQUE,
    deskripsi text,
    syarat text,
    estimasi_hari integer NOT NULL DEFAULT 3 CHECK (estimasi_hari >= 0),
    is_active boolean NOT NULL DEFAULT true,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE kelurahan_info (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nama varchar(255),
    kecamatan varchar(255),
    kota varchar(255),
    provinsi varchar(255),
    kode_pos varchar(20),
    luas_wilayah varchar(100),
    jumlah_penduduk integer NOT NULL DEFAULT 0 CHECK (jumlah_penduduk >= 0),
    jumlah_kk integer NOT NULL DEFAULT 0 CHECK (jumlah_kk >= 0),
    jumlah_laki_laki integer NOT NULL DEFAULT 0 CHECK (jumlah_laki_laki >= 0),
    jumlah_perempuan integer NOT NULL DEFAULT 0 CHECK (jumlah_perempuan >= 0),
    jml_paud_kb_tk integer NOT NULL DEFAULT 0,
    jml_sekolah_dasar integer NOT NULL DEFAULT 0,
    jml_sltp integer NOT NULL DEFAULT 0,
    jml_slta integer NOT NULL DEFAULT 0,
    jml_perguruan_tinggi integer NOT NULL DEFAULT 0,
    jml_rumah_sakit integer NOT NULL DEFAULT 0,
    jml_puskesmas integer NOT NULL DEFAULT 0,
    jml_klinik integer NOT NULL DEFAULT 0,
    jml_klinik_tradisional integer NOT NULL DEFAULT 0,
    jml_posyandu integer NOT NULL DEFAULT 0,
    visi text,
    misi text,
    sejarah text,
    foto_kantor varchar(255),
    alamat_kantor text,
    telp_kantor varchar(20),
    email_kantor varchar(255),
    jam_operasional varchar(255),
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE pengaduan (
    id_pengaduan bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tgl_pengaduan timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    nik char(16) NOT NULL REFERENCES masyarakat(nik) ON DELETE CASCADE ON UPDATE CASCADE,
    tipe_aspirasi varchar(100) NOT NULL,
    judul_laporan varchar(255) NOT NULL,
    isi_laporan text NOT NULL,
    tgl_kejadian timestamptz NOT NULL,
    lokasi_kejadian text NOT NULL,
    latitude numeric(10,8),
    longitude numeric(11,8),
    foto varchar(255) NOT NULL DEFAULT '',
    status varchar(50) NOT NULL DEFAULT 'verifikasi_lapangan'
        CHECK (status IN ('verifikasi_lapangan', 'koordinasi', 'proses_penyelesaian', 'selesai', 'ditolak')),
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE tanggapan (
    id_tanggapan bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_pengaduan bigint NOT NULL REFERENCES pengaduan(id_pengaduan) ON DELETE CASCADE ON UPDATE CASCADE,
    tgl_tanggapan timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    tanggapan text NOT NULL,
    file_bukti varchar(255) NOT NULL DEFAULT '',
    file_bukti_2 varchar(255) NOT NULL DEFAULT '',
    file_bukti_3 varchar(255) NOT NULL DEFAULT '',
    id_petugas bigint NOT NULL REFERENCES petugas(id_petugas) ON UPDATE CASCADE,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE permohonan (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nik char(16) NOT NULL REFERENCES masyarakat(nik) ON DELETE CASCADE ON UPDATE CASCADE,
    id_jenis_layanan bigint NOT NULL REFERENCES jenis_layanan(id),
    tgl_permohonan timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    keterangan text,
    file_ktp varchar(255) NOT NULL DEFAULT '',
    file_kk varchar(255) NOT NULL DEFAULT '',
    file_surat_rtrw varchar(255) NOT NULL DEFAULT '',
    file_foto_rumah varchar(255) NOT NULL DEFAULT '',
    file_pendukung varchar(255) NOT NULL DEFAULT '',
    status varchar(50) NOT NULL DEFAULT 'verifikasi_persyaratan'
        CHECK (status IN ('verifikasi_persyaratan', 'pembuatan_draft', 'penandatanganan', 'register_dokumen', 'selesai', 'ditolak')),
    catatan_petugas text,
    file_hasil varchar(255) NOT NULL DEFAULT '',
    id_petugas bigint REFERENCES petugas(id_petugas) ON DELETE SET NULL,
    tgl_selesai timestamptz,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE permintaan_reset_password (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nik char(16) NOT NULL REFERENCES masyarakat(nik) ON DELETE CASCADE ON UPDATE CASCADE,
    no_hp varchar(20) NOT NULL,
    status varchar(20) NOT NULL DEFAULT 'menunggu' CHECK (status IN ('menunggu', 'selesai')),
    dibaca boolean NOT NULL DEFAULT false,
    catatan text,
    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX regencies_province_id_index ON regencies(province_id);
CREATE INDEX districts_regency_id_index ON districts(regency_id);
CREATE INDEX villages_district_id_index ON villages(district_id);
CREATE INDEX pengaduan_nik_index ON pengaduan(nik);
CREATE INDEX pengaduan_status_index ON pengaduan(status);
CREATE INDEX tanggapan_pengaduan_index ON tanggapan(id_pengaduan);
CREATE INDEX permohonan_nik_index ON permohonan(nik);
CREATE INDEX permohonan_status_index ON permohonan(status);

